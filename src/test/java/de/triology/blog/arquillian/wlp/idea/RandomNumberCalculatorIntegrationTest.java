package de.triology.blog.arquillian.wlp.idea;

import org.jboss.arquillian.container.test.api.Deployment;
import org.jboss.arquillian.junit.Arquillian;
import org.jboss.shrinkwrap.api.ShrinkWrap;
import org.jboss.shrinkwrap.api.asset.EmptyAsset;
import org.jboss.shrinkwrap.api.spec.JavaArchive;
import org.junit.Test;
import org.junit.runner.RunWith;

import javax.inject.Inject;

import static org.junit.Assert.assertNotNull;

@RunWith(Arquillian.class)
public class RandomNumberCalculatorIntegrationTest {

    @Inject
    private RandomNumberCalculator randomNumberCalculator;

    @Deployment
    public static JavaArchive createDeployment() {
        return ShrinkWrap.create(JavaArchive.class)
                         .addClasses(RandomNumberGenerator.class,
                                     RandomNumberCalculator.class)
                         .addAsManifestResource(EmptyAsset.INSTANCE, "beans.xml");
    }

    @Test
    public void addsRandomInts() throws Exception {
        Integer result = randomNumberCalculator.addRandomNumbers();
        assertNotNull(result);
    }
}
